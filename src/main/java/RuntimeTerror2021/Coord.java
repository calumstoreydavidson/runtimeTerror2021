package RuntimeTerror2021;

public class Coord {

    final int x_col;
    final int y_row;

    public Coord(int x_col, int y_row) {
        this.x_col = x_col;
        this.y_row = y_row;
    }

    public int getX_col() {
        return x_col;
    }

    public int getY_row() {
        return y_row;
    }
}
