package RuntimeTerror2021;

public class Coord {

    final int y_row;
    final int x_col;

    public Coord(int y_row, int x_col) {
        this.y_row = y_row;
        this.x_col = x_col;
    }

    public int getY_row() {
        return y_row;
    }

    public int getX_col() {
        return x_col;
    }
}
